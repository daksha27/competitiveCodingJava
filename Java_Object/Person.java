package Java_Object;
public class Person 
{
	private String name;
	private char gender;
	private int age;
	
	public String getName()
	{
		return name;
	}
	public void setName(String _name)
	{
		this.name=_name;
	}
	
	public char getGender()
	{
		return gender;
	}
	public void setGender(char _gender)
	{
		this.gender=_gender;
	}
	
	public int getAge()
	{
		return age;
	}
	public void setAge(int _age)
	{
		this.age=_age;
	}
}
